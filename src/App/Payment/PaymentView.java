    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         String id = t1.getText();
        try {
            //Load the driver
            Class.forName("com.mysql.jdbc.Driver");
            //Create the connection
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/nuwanadb","root","");
            //Create statement
            Statement stmt = con.createStatement();
            //SQL query
            String query = "SELECT * FROM teacher_reg WHERE teacherID = '"+id+"'";
            String query2 = "SELECT * FROM teacher_pay WHERE teacherID = '"+id+ "'";
            
            ResultSet rs2 = stmt.executeQuery(query2);
            rs2.next();
            
            ti.setText(rs2.getString("inst_incom"));
             rs2.close();
            
            //Execute the query
            ResultSet rs = stmt.executeQuery(query);
            rs.next();
            fn.setText(rs.getString("fname"));
            ln.setText(rs.getString("lname"));
            su.setText(rs.getString("subject"));
            eml.setText(rs.getString("email"));
            pn.setText(rs.getString("phno"));
            //txtEmail.setText(rs.getString("Email"));
            rs.close();
            stmt.close();
            con.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

